    public ManagersAddUpdate(int addOrUpdate, int PK) {
        initComponents();
        this.addOrUpdate = addOrUpdate;
        this.PK = PK;
        if (addOrUpdate == 1) {
            jButtonAddUpdate.setText("Изменить");
            this.setTitle("Изменить менеджера");

            ResultSet resSet = null;
            try {
                resSet = RemOtdel.st.executeQuery("select nameofmanager,otcofmanager,famofmanager,login,password"
                        + " from manager where manager.PK_manager=" + PK);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Ошибка: Невозможно изменить");

                this.dispose();
            }
            try {
                if (resSet.next()) {
                    jTextFieldName.setText(resSet.getString(1));
                    jTextFieldOtch.setText(resSet.getString(2));
                    jTextFieldFam.setText(resSet.getString(3));
                    jTextFieldLogin.setText(resSet.getString(4));
                    jTextFieldPassword.setText(resSet.getString(5));
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Ошибка: Невозможно изменить");
                this.dispose();
            }
        }
    }

